character_names = list(characters.keys())

score = 0  # 正解数をカウントする変数
used_characters = []  # 既出のキャラクターを格納するリスト
high_score = 0  # 最高得点を記録する変数
reward_emoji = "\U0001F973"  # 絵文字のUnicodeエスケープシーケンス

print("ブロスタキャラクター当てゲームを開始します。")
print("カタカナで書いて下さい。")
print("個人的な偏見問題があります。気を付けて")
print("終了するには ｆ を入力してください。")

while True:
    # 使用されていないキャラクターを抽選
    remaining_characters = list(set(character_names) - set(used_characters))
    if not remaining_characters:
        print("全てのキャラクターを当てました！")
        break

    random_character = random.choice(remaining_characters)
    description = characters[random_character]

    print(f"\nキャラクターの特徴: {description}")

    user_answer = input("キャラクター名を入力してください: ")

    if user_answer.lower() == random_character.lower():
        print("正解！")
        score += 1
        if score > high_score:
            high_score = score
            print("新しい最高得点！" + reward_emoji)
    elif user_answer.lower() == "ｆ":
        break
    else:
        print("不正解！正解は " + random_character + " です。")
        score -= 1

    # 使用されたキャラクターを追加
    used_characters.append(random_character)

print("\n結果")
print(f"正解数: {score}")
print(f"最高得点: {high_score}")